package unico;

public class Mensaje {

	private int mensaje;
	
	
	public Mensaje(int pMen)
	{
		mensaje = pMen;
	}
	

	/**
	 * @return the mensaje
	 */
	public int getMensaje() {
		return mensaje;
	}

	/**
	 * @param mensaje the mensaje to set
	 */
	public void setMensaje(int mensaje) {
		this.mensaje = mensaje;
	}
	
	
	
	
	
}
